def get_language(request: Request) -> str:
    raw_value = (request.cookies.get("openeelo_lang") or DEFAULT_LANGUAGE).strip().lower()
    return raw_value if raw_value in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def translate(language: str, text: str) -> str:
    if language == "de":
        return text
    return _TRANSLATIONS.get(text, {}).get(language, text)
